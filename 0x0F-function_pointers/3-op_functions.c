#include "3-calc.h"
/**
 * op_add - adds 2 numbers
 * @a: first variable
 * @b: Second variable
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtructs 2 numbers
 * @a: first variable
 * @b: second variable
 * Return: the difference of 2 numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mulptiplies 2 numbers
 * @a: first variable
 * @b: second variable
 * Return: The multiplication of 2 numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divides 2 numbers
 * @a: First variable
 * @b: Second variable
 * Return: the division of 2 numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder after division
 * @a: first variable
 * @b: second variable
 * Return: The remainder after division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
