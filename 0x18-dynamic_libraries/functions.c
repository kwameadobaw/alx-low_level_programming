#include "main.h"
/**
 * add - add two numbers
 *
 * @a: variable to be added
 *
 * @b: variable to be added
 * Return: Return the sum of the numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * mul - check the code.
 *
 * @a: First number
 *
 * @b: Second number
 *
 * Return: Always 0.
 */
int mul(int a, int b)
{

	return (a * b);
}

/**
 * sub - check the code.
 *
 * @a: First number
 *
 * @b: Second number
 *
 * Return: Always 0.
 */
int sub(int a, int b)
{

	return (a - b);
}

/**
 * div - check the code.
 *
 * @a: First number
 *
 * @b: Second number
 *
 * Return: Always 0.
 */
int div(int a, int b)
{

	return (a / b);
}

/**
 * mod - check the code.
 *
 * @a: First number
 *
 * @b: Second number
 *
 * Return: Always 0.
 */
int mod(int a, int b)
{

	return (a % b);
}
